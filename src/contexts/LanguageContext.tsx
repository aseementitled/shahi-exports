interface LanguageProviderProps {
  children: ReactNode;
}

export const LanguageProvider: React.FC<LanguageProviderProps> = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState<SupportedLanguage>('en');

  // Load language preference from localStorage on mount
  useEffect(() => {
    const savedLanguage = localStorage.getItem('selectedLanguage') as SupportedLanguage;
    if (savedLanguage && ['en', 'hi', 'te', 'kn', 'mr', 'hinglish'].includes(savedLanguage)) {
      setCurrentLanguage(savedLanguage);
    }
  }, []);

  const setLanguage = (language: SupportedLanguage) => {
    setCurrentLanguage(language);
    localStorage.setItem('selectedLanguage', language);
  };

  const t = (key: string, section: string = 'common'): string | string[] => {
    try {
      const sectionTranslations = translations[section as keyof typeof translations];
      if (!sectionTranslations) {
        console.warn(`Translation section '${section}' not found`);
        return key;
      }

      // Always fallback to English if the current language doesn't exist in this section
      const supportedLanguages = ['en', 'hi'] as const;
      const effectiveLanguage = supportedLanguages.includes(currentLanguage as 'en' | 'hi') ? currentLanguage : 'en';
      
      const languageTranslations = sectionTranslations[effectiveLanguage as 'en' | 'hi'];
      if (!languageTranslations) {
        // Final fallback to English
        const englishTranslations = sectionTranslations['en'];
        return (englishTranslations as Record<string, string | string[]>)?.[key] || key;
      }

      return (languageTranslations as Record<string, string | string[]>)?.[key] || key;
    } catch (error) {
      console.warn(`Translation error for key '${key}' in section '${section}':`, error);
      return key;
    }
  };

  const value = {
    currentLanguage,
    setLanguage,
    t,
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

export default LanguageContext;
